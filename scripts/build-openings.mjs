/**
 * Downloads the Lichess chess-openings TSV dataset, converts PGN moves to UCI,
 * and writes src/openings/openingBook.js as a static JS module.
 *
 * Run once: node scripts/build-openings.mjs
 */

import { Chess } from '../node_modules/chess.js/dist/esm/chess.js';
import { writeFileSync } from 'fs';

const FILES = ['a', 'b', 'c', 'd', 'e'];
const BASE = 'https://raw.githubusercontent.com/lichess-org/chess-openings/master/';

function pgnToUci(pgn) {
  const game = new Chess();
  const tokens = pgn.trim().split(/\s+/).filter(t => t && !/^\d+\./.test(t));
  const uci = [];
  for (const san of tokens) {
    try {
      const move = game.move(san);
      if (!move) return null;
      uci.push(move.from + move.to + (move.promotion ?? ''));
    } catch {
      return null;
    }
  }
  return uci.join(' ');
}

console.log('Downloading openings from lichess-org/chess-openings…');
const book = {};

for (const file of FILES) {
  const res = await fetch(`${BASE}${file}.tsv`);
  if (!res.ok) { console.error(`Failed to fetch ${file}.tsv`); continue; }
  const text = await res.text();
  const lines = text.trim().split('\n').slice(1); // skip header row

  for (const line of lines) {
    const [eco, name, pgn] = line.split('\t');
    if (!eco || !name || !pgn) continue;
    const uci = pgnToUci(pgn);
    if (uci) book[uci] = { eco, name };
  }
  console.log(`  ${file}.tsv — OK`);
}

const count = Object.keys(book).length;
console.log(`\nConverted ${count} opening variations.`);

const output = `// Auto-generated by scripts/build-openings.mjs — do not edit manually.
// Source: https://github.com/lichess-org/chess-openings (CC0)
export const OPENING_BOOK = /** @type {Record<string,{eco:string,name:string}>} */ (
${JSON.stringify(book)}
);
`;

writeFileSync(new URL('../src/openings/openingBook.js', import.meta.url), output);
console.log('Written to src/openings/openingBook.js');
